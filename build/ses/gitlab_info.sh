#!/bin/bash
# \"`git rev-parse --abbrev-ref HEAD`_`git describe`\"
#GIT_VER = \"`git rev-parse --abbrev-ref HEAD`_`git describe`\"
a="`git rev-parse --abbrev-ref HEAD`"
b="_"
c="`git describe`"
echo "$a$b$c" | awk ' BEGIN {print "#include \"version.h\"\r\n\r\n/* File generated by automatically. Any manual change will be overwritten. */"} {print "char *cGitVersion = \"" $$0" \" __DATE__ \" \" __TIME__;"} END {}' > ../../version_unix.c
awk 'sub("$", "\r")' ../../version_unix.c > ../../version.c
rm ../../version_unix.c
cp -f ../../source/tracemsg.h '../../../../../../../UART sniffer/tracemsg.h'
